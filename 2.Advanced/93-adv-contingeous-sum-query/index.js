function sumquery(A, B) {
  let arr = Array(A).fill(0);

  for (let i = 0; i < B.length; i++) {
    let start = Number(B[i][0]);
    let end = Number(B[i][1]);
    let amount = Number(B[i][2]);

    arr[start - 1] = arr[start - 1] + amount;
    if (end !== A) {
      arr[end] = arr[end] - amount;
    }
  }

  // prefix sum array
  for (let i = 1; i < A; i++) {
    arr[i] = arr[i - 1] + arr[i];
  }

  return arr;
}

